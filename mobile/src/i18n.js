const LangueContexte = createContext({ langue: "fr", t: (k) => k, setLangue: () => {} });

export function LangueProvider({ children }) {
  const [langue, setLangueEtat] = useState("fr");
  React.useEffect(() => {
    AsyncStorage.getItem("asm_langue").then((l) => {
      if (l === "ar" || l === "fr") setLangueEtat(l);
    }).catch(() => {});
  }, []);
  const setLangue = useCallback((l) => {
    setLangueEtat(l);
    AsyncStorage.setItem("asm_langue", l).catch(() => {});
  }, []);
  const t = useCallback((cle) => T[langue][cle] ?? T.fr[cle] ?? cle, [langue]);
  return <LangueContexte.Provider value={{ langue, t, setLangue }}>{children}</LangueContexte.Provider>;
}

export const useLangue = () => useContext(LangueContexte);
